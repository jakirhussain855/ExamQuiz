public class Loops {
    public static void main(String[] args) {


        for (int a = 0; a < 5; a++) {
            System.out.println("books");
        }
        for (int a = 0; a < 5; a++) {
            System.out.println(a + "books  ");
        }

        //while loop
        int x=0;
        while(x<5){
            System.out.println( x +"  java books");
            x++;
        }

    }
}